package com.cascade;

import java.util.ArrayList;
import java.util.Collection;

import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;

@Entity
public class User {

	@Id @GeneratedValue(strategy=GenerationType.AUTO)
	private int userID;
	
	private String userName;
	
	
	//purpose of cascading is to get the independent objects(vehicles) saved automatically when 
	//we are saving the dependent ones(user)
	@OneToMany(cascade=CascadeType.ALL)
	private Collection<Vehicle> vehicleList=new ArrayList<>();
	
	public Collection<Vehicle> getVehicleList() {
		return vehicleList;
	}

	public void setVehicleList(Collection<Vehicle> vehicleList) {
		this.vehicleList = vehicleList;
	}

	public int getUserID() {
		return userID;
	}

	public void setUserID(int userID) {
		this.userID = userID;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
}
